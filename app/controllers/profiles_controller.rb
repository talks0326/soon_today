class ProfilesController < ApplicationController
  protect_from_forgery :except => [:user_show,:self_update,:self_edit]
  before_action :set_profile, only: [:show, :edit, :update, :destroy]

  # GET /profiles
  # GET /profiles.json
  def index
    @profiles = Profile.all
  end

  # GET /profiles/1
